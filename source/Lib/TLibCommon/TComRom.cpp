Void generateScanPattern(Int scanType, UInt* pBuf, Int blkWidth, Int blkHeight, Int startPos, Int stride)
{
  switch(scanType)
  {
  case SCAN_DIAG:
    {
      const UInt  uiNumScanPos  = (UInt)(blkHeight * blkWidth);
      UInt        uiNextScanPos = 0;
      for( UInt uiScanLine = 0; uiNextScanPos < uiNumScanPos; uiScanLine++ )
      {
        Int    iPrimDim  = (Int)uiScanLine;
        Int    iScndDim  = 0;
        while( iPrimDim >= blkHeight )
        {
          iPrimDim--;
          iScndDim++;
        }
        while( iPrimDim >= 0 && iScndDim < blkWidth )
        {
          *pBuf++ = (startPos+ (iPrimDim * stride) + iScndDim) ;
          uiNextScanPos++;
          iPrimDim--;
          iScndDim++;
        }
      }
    }
    break;
  case SCAN_HOR:
    {
      for(Int y=0; y< blkHeight; y++)
      {
        for(Int x=0; x< blkWidth; x++)
        {
          *pBuf++ = (startPos+ (y* stride) + x);
        }
      }
    }
    break;
  case SCAN_VER:
    {
      for(Int x=0; x< blkWidth; x++)
      {
        for(Int y=0; y< blkHeight; y++)
        {
          *pBuf++ = (startPos+ (y* stride) + x);
        }
      }
    }
    break;
  default:
    {
      printf("not a supported scan type\n");
      exit(-1);
    }
  }

}


Void initCoefScan (  Int scanType
  , UInt* targetCGScan  //input: coefficient group scan
  , UInt* targetCoefScan  //output: coefficient scan
  , Int widthLog2
  , Int heightLog2
  , Int cgSizeLog2
  )
{
  Int   tuWidth = (1<< g_transInfo[widthLog2][heightLog2].widthLog2);
  Int   tuHeight= (1<< g_transInfo[widthLog2][heightLog2].heightLog2);

  Int numCGInWidth  = (tuWidth >> cgSizeLog2);
  Int numCGInHeight = (tuHeight>> cgSizeLog2);
  Int numCGInTU     = numCGInWidth* numCGInHeight;

  //coefficient scan
  for(Int cg=0; cg< numCGInTU; cg++)
  {
    Int cgPos = targetCGScan[cg];
    Int coefPosY = (cgPos/numCGInWidth)<< cgSizeLog2;
    Int coefPosX = (cgPos%numCGInWidth)<< cgSizeLog2;
    Int coefPos  = coefPosX + (coefPosY*tuWidth);

    generateScanPattern(scanType, targetCoefScan, (1<<cgSizeLog2), (1<<cgSizeLog2), coefPos, tuWidth);

    targetCoefScan += (1<< (cgSizeLog2*2));
  }
}


Void createTransformInfo()
{
  //initialize all elements NULL
  memset(g_sigCoefScan, 0, sizeof(UInt*)*NUM_COEFF_SCAN_TYPE*(CTU_LOG2+1)*(CTU_LOG2+1));
  memset(g_sigCoefGroupScan, 0, sizeof(UInt*)*NUM_COEFF_SCAN_TYPE*(CTU_LOG2+1)*(CTU_LOG2+1));

  Int tuWidthLog2, tuHeightLog2;
  for ( tuWidthLog2=1; tuWidthLog2 < CTU_LOG2+1; tuWidthLog2++)
  {
    for( tuHeightLog2=1;  tuHeightLog2 < CTU_LOG2+1; tuHeightLog2++)
    {
      Int cgSideLog2 = (tuWidthLog2==1 || tuHeightLog2==1)? 1: 2;

      g_transInfo[tuWidthLog2][tuHeightLog2].widthLog2 = tuWidthLog2 ;
      g_transInfo[tuWidthLog2][tuHeightLog2].heightLog2= tuHeightLog2;

      //alloc memory
      for(Int scanType=0; scanType < (tuWidthLog2+tuHeightLog2<=6 ? NUM_COEFF_SCAN_TYPE: 1); scanType++)  //need to check here
      {
        g_sigCoefScan        [ scanType ][ tuWidthLog2][tuHeightLog2 ]= new UInt[1<< (tuWidthLog2 + tuHeightLog2)];
        g_sigCoefGroupScan   [ scanType ][ tuWidthLog2][tuHeightLog2 ]= new UInt[1<< (tuWidthLog2-cgSideLog2 + tuHeightLog2 - cgSideLog2)];

        //cg scan
        generateScanPattern(scanType
          , g_sigCoefGroupScan[scanType][tuWidthLog2][tuHeightLog2]
        , (1<< (tuWidthLog2 - cgSideLog2))
          , (1<< (tuHeightLog2 - cgSideLog2))
          , 0
          , (1<< (tuWidthLog2 - cgSideLog2))
          );
        //coefficient scan
        initCoefScan (  scanType
          , g_sigCoefGroupScan[scanType][tuWidthLog2][tuHeightLog2]  
        , g_sigCoefScan[scanType][tuWidthLog2][tuHeightLog2]
        , tuWidthLog2
          ,tuHeightLog2
          , cgSideLog2
          );
      }
    }
  }
}


Void destroyTransformInfo()
{
  //initialize all elements NULL
  for(Int scanType=0; scanType< NUM_COEFF_SCAN_TYPE; scanType++)
  {
    for (Int i=0; i<CTU_LOG2+1; i++)
    {
      for (Int j=0; j<CTU_LOG2+1; j++)
      {
        if(g_sigCoefScan[ scanType ][ i ][ j ])
        {
          delete[] g_sigCoefScan[ scanType ][ i ][ j ]; g_sigCoefScan[ scanType ][ i ][ j ]= NULL;
        }
        if(g_sigCoefGroupScan[ scanType ][ i ][ j ])
        {
          delete[] g_sigCoefGroupScan[ scanType ][ i ][ j ]; g_sigCoefGroupScan[ scanType ][ i ][ j ]= NULL;
        }
      }
    }
  }
}

